package week1.day2;

public class CheckNumberIsPositive {
	
	public static void main(String[] args) {
	   int version=10; // Declaring the version variable
	   if(version>0) //checking the value is greater than zero in the conditional statement
	   {
		   System.out.println("The version value "+ version+" is positive");//if the value is greater than zero , printing it as positive
	   }
	   else
	   {
		   System.out.println("The version value "+ version+" is negative");//if the value is lesser than zero, printing it as negative
	   }
       
	}

}
